# Tipos int e float
# int -> Número inteiro
# O tipo int representa qualquer número 
# posito ou negativo. int sem sinal é considerado
# positivo.

print(11) # int
print(-11) # int
print(0) # int

# float -> Número com ponto flutuante
# O tipo float representa qualquer número
# posito ou negativo com ponto flutuante.
# float sem sinal é considerado positivo.

print(1.1) # float
print(1.1) # float # números sempre são separados por . e , é quando querendo exibir mais de uma coisa na tela.
print(1.1, 10.11)
print(0.0, -1.5) # float

# A função type mostra o tipo que o python
# inferiu ao valor.
print(type('Victor'))
print(type(0))
print(type(1.1), type(-1.1), type(0.0))

