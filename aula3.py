"""
Python= Linguagem de programação
Tipo de tripagem = Dinâmica / Forte
Str -> string -> texto
String são textos que estão dentro de aspas
"""
print(1234)

# Aspas simples
print('Victor Oliveira')
print(1, 'Victor "Oliveira"')


# Aspas duplas
print("Victor Oliveira")
print(2, "Victor 'Oliveira'")


# Escape
print("Victor \"Oliveira\"")

# r
print(r"Victor \"Oliveira\"")
